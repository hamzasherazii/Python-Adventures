# For loop in list
l = [1, 2, 3, 4, 5]
for i in l:
    print(i)

print("-----------------------------")

# For loop in tuple
t = (1, 2, 3, 4, 5)
for i in t:
    print(i)


print("-----------------------------")

# For loop with else
# else is printed when the loop finishes
forloop = [1, 2, 3, 4, 5]
for i in forloop:
    print(i)
else:
    print("Loop is finished")